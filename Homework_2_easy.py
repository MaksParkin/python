# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.
# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()

print('Вывод нумерованного списка фруктов')
fruits = ['orange', 'lemon', 'pineapple', 'mango', 'pear']
for i, val in enumerate(fruits, 1):
    print("{:d}. {:>}".format(i, val))


