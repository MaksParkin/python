# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

print('Изменение элементов спсика в зависимости от их кратности 2')
list_num = [234, 52, 567, 7895, 23, 12, 373578]
for v,i in enumerate(list_num):
    if i % 2 == 0:
        list_num[v] = i / 4
    elif i % 2 != 0:
        list_num[v] = i * 2
print(list_num)