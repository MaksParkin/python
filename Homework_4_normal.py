num_list = [random.randint(0, 9) for _ in range(2500)]
num = int(''.join(str(i) for i in num_list))
path = os.path.join('.', "number.txt")

with open(path, 'w', encoding='UTF-8') as f:
    f.write(str(num))

with open(path, 'r', encoding='UTF-8') as f:
    num_str = f.read()

num_list = list(num_str)

num_list2 = max((list(y) for x, y in itertools.groupby(num_list)), key=len)
max_num = int(''.join(str(i) for i in num_list2))
